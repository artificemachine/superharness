#!/bin/bash
# superharness Stop hook for Claude Code
# Fires automatically when a session ends. Writes a session-progress.md snapshot
# so the next session-start.sh can restore context without agent cooperation.

if [ -n "$CLAUDE_PLUGIN_ROOT" ]; then
  SUPERHARNESS_ROOT="$(cd "$CLAUDE_PLUGIN_ROOT/../.." && pwd)"
else
  SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
  SUPERHARNESS_ROOT="$(cd "$SCRIPT_DIR/../../.." && pwd)"
fi

PROJECT_DIR="$(pwd)"
SH_DIR="$PROJECT_DIR/.superharness"

# Only act in superharness-initialized projects
[ -d "$SH_DIR" ] || exit 0

PROGRESS_FILE="$SH_DIR/session-progress.md"
TIMESTAMP="$(date -u +"%Y-%m-%dT%H:%M:%SZ" 2>/dev/null || date +"%Y-%m-%dT%H:%M:%SZ")"

# Helper: append a line to ledger.md if it exists
_ledger() { [ -f "$SH_DIR/ledger.md" ] && echo "$1" >> "$SH_DIR/ledger.md"; }

# --- Gather state from disk (no agent cooperation needed) ---

# Active task info via shux context (reuses the same aggregator as session-start)
TASK_CONTEXT=""
if command -v superharness >/dev/null 2>&1; then
  TASK_CONTEXT=$(superharness context --project "$PROJECT_DIR" 2>/dev/null || true)
fi
if [ -z "$TASK_CONTEXT" ]; then
  # Fallback: inline Python to extract minimal task info
  TASK_CONTEXT=$(python3 -c "
import sys
try:
    import yaml
    doc = yaml.safe_load(open('$SH_DIR/contract.yaml'))
    tasks = doc.get('tasks') or []
    for t in tasks:
        if isinstance(t, dict):
            s = t.get('status','')
            if s in ('in_progress','plan_proposed','plan_approved','report_ready'):
                print(f\"Active task: {t.get('id','?')} ({s}) — {t.get('title','')}\")
                break
    else:
        print('No active task found.')
except: print('Could not read contract.')
" 2>/dev/null || echo "Could not read contract.")
fi

# Git state (branch, uncommitted changes, recent commits)
GIT_BRANCH=""
GIT_STATUS=""
GIT_LOG=""
if git -C "$PROJECT_DIR" rev-parse --is-inside-work-tree >/dev/null 2>&1; then
  GIT_BRANCH=$(git -C "$PROJECT_DIR" branch --show-current 2>/dev/null || true)
  GIT_STATUS=$(git -C "$PROJECT_DIR" status --short 2>/dev/null | head -20 || true)
  GIT_LOG=$(git -C "$PROJECT_DIR" log --oneline -5 2>/dev/null || true)
fi

# --- Write session-progress.md ---

cat > "$PROGRESS_FILE" <<PROGRESS_EOF
# Session Progress
<!-- Auto-generated by superharness session-stop hook. Do not edit manually. -->
<!-- Last updated: ${TIMESTAMP} -->

## Task Context
${TASK_CONTEXT}

## Branch
${GIT_BRANCH:-"(not a git repo or detached HEAD)"}

## Uncommitted Changes
${GIT_STATUS:-"(clean)"}

## Recent Commits
${GIT_LOG:-"(none)"}

## Note
This file was written automatically when the previous Claude Code session ended.
If the session was long and context was compacted, some nuance may have been lost.
Read .superharness/handoffs/ for the most authoritative cross-session state.
PROGRESS_EOF

# Append a ledger entry recording the session end
_ledger "$TIMESTAMP session-stop: snapshot written to session-progress.md (branch: ${GIT_BRANCH:-unknown})"

# --- Pause active inbox items (pending / launched / running) ---
INBOX_FILE="$SH_DIR/inbox.json"
if [ -f "$INBOX_FILE" ] && command -v python3 >/dev/null 2>&1; then
  NOW="$(date -u +"%Y-%m-%dT%H:%M:%SZ" 2>/dev/null || date +"%Y-%m-%dT%H:%M:%SZ")"
  PAUSABLE_IDS="$(python3 -c "
import yaml, sys
try:
    items = yaml.safe_load(open('$INBOX_FILE').read()) or []
    for item in (items if isinstance(items, list) else []):
        if isinstance(item, dict) and item.get('status') in ('pending', 'launched', 'running'):
            print(item.get('id', ''))
except Exception as e:
    print('warn:', e, file=sys.stderr)
" 2>/dev/null || true)"
  for ITEM_ID in $PAUSABLE_IDS; do
    [ -z "$ITEM_ID" ] && continue
    python3 -m superharness.engine.inbox set_status \
      --file "$INBOX_FILE" --id "$ITEM_ID" \
      --from pending --to paused --now "$NOW" --stamp-key paused_at 2>/dev/null || \
    python3 -m superharness.engine.inbox set_status \
      --file "$INBOX_FILE" --id "$ITEM_ID" \
      --from launched --to paused --now "$NOW" --stamp-key paused_at 2>/dev/null || \
    python3 -m superharness.engine.inbox set_status \
      --file "$INBOX_FILE" --id "$ITEM_ID" \
      --from running --to paused --now "$NOW" --stamp-key paused_at 2>/dev/null || true
    _ledger "$TIMESTAMP session-stop: inbox item paused ($ITEM_ID)"
  done
fi

# --- Revert in_progress contract tasks to todo ---
if [ -f "$SH_DIR/contract.yaml" ] && command -v python3 >/dev/null 2>&1; then
  python3 -c "
import sys
try:
    try:
        import ruamel.yaml as _ry
        _yaml = _ry.YAML()
        _yaml.preserve_quotes = True
        import io as _io
        with open('$SH_DIR/contract.yaml') as _f:
            _doc = _yaml.load(_f)
        _changed = False
        for _t in (_doc.get('tasks') or []):
            if isinstance(_t, dict) and _t.get('status') == 'in_progress':
                _t['status'] = 'todo'
                _changed = True
        if _changed:
            _buf = _io.StringIO()
            _yaml.dump(_doc, _buf)
            with open('$SH_DIR/contract.yaml', 'w') as _f:
                _f.write(_buf.getvalue())
    except ImportError:
        import yaml as _pyyaml
        with open('$SH_DIR/contract.yaml') as _f:
            _doc = _pyyaml.safe_load(_f)
        _changed = False
        for _t in (_doc.get('tasks') or []):
            if isinstance(_t, dict) and _t.get('status') == 'in_progress':
                _t['status'] = 'todo'
                _changed = True
        if _changed:
            with open('$SH_DIR/contract.yaml', 'w') as _f:
                _pyyaml.dump(_doc, _f, default_flow_style=False)
except Exception as _e:
    print(f'warn: could not revert contract tasks: {_e}', file=sys.stderr)
" 2>/dev/null || true
fi

# --- Unload launchd watcher for this project (macOS only) ---
if [ "$(uname -s)" = "Darwin" ]; then
  PROJECT_SLUG="$(basename "$PROJECT_DIR" | tr -cs 'A-Za-z0-9' '-')"
  LABEL="com.superharness.inbox.${PROJECT_SLUG}"
  PLIST_PATH="$HOME/Library/LaunchAgents/${LABEL}.plist"
  if [ -f "$PLIST_PATH" ] && launchctl list "$LABEL" >/dev/null 2>&1; then
    launchctl unload "$PLIST_PATH" >/dev/null 2>&1 || true
    _ledger "$TIMESTAMP session-stop: watcher unloaded ($LABEL)"
  fi
fi

exit 0
