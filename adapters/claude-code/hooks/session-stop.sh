#!/bin/bash
# superharness Stop hook for Claude Code
# Fires automatically when a session ends. Writes a session-progress.md snapshot
# so the next session-start.sh can restore context without agent cooperation.

if [ -n "$CLAUDE_PLUGIN_ROOT" ]; then
  SUPERHARNESS_ROOT="$(cd "$CLAUDE_PLUGIN_ROOT/../.." && pwd)"
else
  SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
  SUPERHARNESS_ROOT="$(cd "$SCRIPT_DIR/../../.." && pwd)"
fi

PROJECT_DIR="$(pwd)"
SH_DIR="$PROJECT_DIR/.superharness"

# Only act in superharness-initialized projects
[ -d "$SH_DIR" ] || exit 0

PROGRESS_FILE="$SH_DIR/session-progress.md"
TIMESTAMP="$(date -u +"%Y-%m-%dT%H:%M:%SZ" 2>/dev/null || date +"%Y-%m-%dT%H:%M:%SZ")"

# --- Gather state from disk (no agent cooperation needed) ---

# Active task info via shux context (reuses the same aggregator as session-start)
TASK_CONTEXT=""
if command -v superharness >/dev/null 2>&1; then
  TASK_CONTEXT=$(superharness context --project "$PROJECT_DIR" 2>/dev/null || true)
fi
if [ -z "$TASK_CONTEXT" ]; then
  # Fallback: inline Python to extract minimal task info
  TASK_CONTEXT=$(python3 -c "
import sys
try:
    import yaml
    doc = yaml.safe_load(open('$SH_DIR/contract.yaml'))
    tasks = doc.get('tasks') or []
    for t in tasks:
        if isinstance(t, dict):
            s = t.get('status','')
            if s in ('in_progress','plan_proposed','plan_approved','report_ready'):
                print(f\"Active task: {t.get('id','?')} ({s}) — {t.get('title','')}\")
                break
    else:
        print('No active task found.')
except: print('Could not read contract.')
" 2>/dev/null || echo "Could not read contract.")
fi

# Git state (branch, uncommitted changes, recent commits)
GIT_BRANCH=""
GIT_STATUS=""
GIT_LOG=""
if git -C "$PROJECT_DIR" rev-parse --is-inside-work-tree >/dev/null 2>&1; then
  GIT_BRANCH=$(git -C "$PROJECT_DIR" branch --show-current 2>/dev/null || true)
  GIT_STATUS=$(git -C "$PROJECT_DIR" status --short 2>/dev/null | head -20 || true)
  GIT_LOG=$(git -C "$PROJECT_DIR" log --oneline -5 2>/dev/null || true)
fi

# --- Write session-progress.md ---

cat > "$PROGRESS_FILE" <<PROGRESS_EOF
# Session Progress
<!-- Auto-generated by superharness session-stop hook. Do not edit manually. -->
<!-- Last updated: $TIMESTAMP -->

## Task Context
$TASK_CONTEXT

## Branch
${GIT_BRANCH:-"(not a git repo or detached HEAD)"}

## Uncommitted Changes
${GIT_STATUS:-"(clean)"}

## Recent Commits
${GIT_LOG:-"(none)"}

## Note
This file was written automatically when the previous Claude Code session ended.
If the session was long and context was compacted, some nuance may have been lost.
Read .superharness/handoffs/ for the most authoritative cross-session state.
PROGRESS_EOF

# Append a ledger entry recording the session end
if [ -f "$SH_DIR/ledger.md" ]; then
  echo "$TIMESTAMP session-stop: snapshot written to session-progress.md (branch: ${GIT_BRANCH:-unknown})" >> "$SH_DIR/ledger.md"
fi

exit 0
